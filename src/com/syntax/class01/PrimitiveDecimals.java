package com.syntax.class01;

public class PrimitiveDecimals {
	public static void main (String[]args) {
		
		//8 primitive datatypes
		//to store whole number --> byte ,short,int,long 
		byte anyname=127;
		short anyname1=32767;
		int anyname2=10000;
		long anyname3=20000;
		
		//to store decimal numbers --> Float , double
		float anyname01=12.99f;
		double anyname02=132.34234;
		
		//to store single character --> char 
		char anyname001='F';
		char anyname002='&';
		
		//to store boolean values -->boolean
		boolean hungry=true;
		boolean thirsty=false;
		
		System.out.println(anyname2);
		System.out.println(anyname002);
		System.out.println(hungry);
		System.out.println(anyname1);
		
	
	}

}
